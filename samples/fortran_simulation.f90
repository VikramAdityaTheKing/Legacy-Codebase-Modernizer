      PROGRAM PRESSURE_SIM
      IMPLICIT NONE
      REAL*8 :: INPUT, FACTOR, RESULT
      INPUT = 4500.0
      FACTOR = 1.18
      IF (INPUT .GT. 4000.0) THEN
         RESULT = INPUT * FACTOR
      ELSE
         RESULT = INPUT
      END IF
      WRITE(*,*) 'RESULT:', RESULT
      END PROGRAM PRESSURE_SIM
